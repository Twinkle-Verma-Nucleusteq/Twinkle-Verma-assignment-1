package blogportal.application;

/**
 * Enum representing the status of blog posts.
 */
public enum Status {
  /**
   * Represents an approved blog post.
   */
  STATUS,
    /**
     * Represents an approved blog post.
     */
    APPROVED,

    /**
     * Represents a pending blog post awaiting review.
     */
    PENDING,

    /**
     * Represents a rejected blog post.
     */
    REJECTED
}
